struct cubeLight {
    glm::vec3 position;
    glm::vec3 ambient;
    glm::vec3 diffuse;
    glm::vec3 specular;
    float constant;
    float linear;
    float quadratic;
    glm::vec3 color;

    cubeLight(glm::vec3 position,
            glm::vec3 ambient,
            glm::vec3 diffuse,
            glm::vec3 specular,
            float constant,
            float linear,
            float quadratic,
            glm::vec3 color) {
        this->ambient = ambient;
        this->position = position;
        this->diffuse = diffuse;
        this->specular = specular;
        this->constant = constant;
        this->linear = linear;
        this->quadratic = quadratic;
        this->color = color;
    }
};